import { useState, useEffect } from 'react';
import {  FaChild, FaHeart, FaHome, FaPaw, FaBook } from 'react-icons/fa';
import { ToastContainer, toast } from 'react-toastify';
const HeroSection = () => {
  const [activeForm, setActiveForm] = useState('care');
  const [step, setStep] = useState(1);
  const [formData, setFormData] = useState({
    zipCode: '',
    careDate: '',
    careType: '',
    whoNeedsCare: '',
    additionalCare: [],
    firstName: '',
    lastName: '',
    email: '',
  });
  const [errors, setErrors] = useState({});
  const [showToast, setShowToast] = useState(false);

  useEffect(() => {
    if (showToast) {
      const timer = setTimeout(() => setShowToast(false), 3000);
      return () => clearTimeout(timer);
    }
  }, [showToast]);

  const resetForm = () => {
    setFormData({
      zipCode: '',
      careDate: '',
      careType: '',
      whoNeedsCare: '',
      additionalCare: [],
      firstName: '',
      lastName: '',
      email: '',
    });
    setStep(1);
    setErrors({});
  };

  const validate = () => {
    const newErrors = {};
    if (activeForm === 'care') {
      switch (step) {
        case 1:
          if (!formData.zipCode) newErrors.zipCode = 'ZIP code is required';
          break;
        case 2:
          if (!formData.careDate) newErrors.careDate = 'Care date is required';
          break;
        case 3:
          if (!formData.careType) newErrors.careType = 'Care type is required';
          break;
        case 4:
          if (!formData.whoNeedsCare) newErrors.whoNeedsCare = 'Who needs care is required';
          break;
        case 5:
          if (formData.additionalCare.length === 0) newErrors.additionalCare = 'Please select at least one additional care option';
          break;
        case 6:
          if (!formData.email) newErrors.email = 'Email is required';
          else if (!/\S+@\S+\.\S+/.test(formData.email)) newErrors.email = 'Email is invalid';
          break;
        default:
          break;
      }
    } else {
      switch (step) {
        case 1:
          if (!formData.zipCode) newErrors.zipCode = 'ZIP code is required';
          break;
        case 2:
          if (!formData.firstName) newErrors.firstName = 'First name is required';
          if (!formData.lastName) newErrors.lastName = 'Last name is required';
          break;
        case 3:
          if (!formData.email) newErrors.email = 'Email is required';
          else if (!/\S+@\S+\.\S+/.test(formData.email)) newErrors.email = 'Email is invalid';
          break;
        default:
          break;
      }
    }
    setErrors(newErrors);
    return Object.keys(newErrors).length === 0;
  };

  const handleNext = () => {
    if (validate()) {
      if ((activeForm === 'care' && step < 6) || (activeForm === 'job' && step < 3)) {
        setStep(step + 1);
      } else {
        // Form submission
        console.log('Form submitted:', formData);
        toast.success("Thank you for form submission")
        resetForm();
      }
      window.sessionStorage.setItem('formData', JSON.stringify(formData));
    }
  };

  const handleChange = (e) => {
    const { name, value } = e.target;
    setFormData((prev) => ({ ...prev, [name]: value }));
  };

  const handleCheckboxChange = (e) => {
    const { name, value, checked } = e.target;
    setFormData((prev) => {
      const selectedOptions = [...prev[name]];
      if (checked) {
        selectedOptions.push(value);
      } else {
        const index = selectedOptions.indexOf(value);
        if (index > -1) selectedOptions.splice(index, 1);
      }
      return { ...prev, [name]: selectedOptions };
    });
  };

  const renderForm = () => {
    if (activeForm === 'care') {
      switch (step) {
        case 1:
          return (
            <>
              <h2 className="text-lg mb-4 text-white">Where do you need care?</h2>
              <input
                type="text"
                name="zipCode"
                placeholder="ZIP code"
                value={formData.zipCode}
                onChange={handleChange}
                className="w-full p-4 rounded-lg border border-gray-300 text-gray-900 focus:outline-none focus:ring-2 focus:ring-teal-400 mb-4"
              />
              {errors.zipCode && <p className="text-red-500 text-sm mb-2">{errors.zipCode}</p>}
            </>
          );
        case 2:
          return (
            <>
              <h2 className="text-lg mb-4 text-white">When do you need care?</h2>
              <select
                name="careDate"
                value={formData.careDate}
                onChange={handleChange}
                className="w-full p-4 rounded-lg border border-gray-300 text-gray-900 focus:outline-none focus:ring-2 focus:ring-teal-400 mb-4"
              >
                <option value="" disabled>Select a date</option>
                <option value="Today">Today</option>
                <option value="Tomorrow">Tomorrow</option>
                <option value="This Week">This Week</option>
                <option value="Next Week">Next Week</option>
              </select>
              {errors.careDate && <p className="text-red-500 text-sm mb-2">{errors.careDate}</p>}
            </>
          );
        case 3:
          return (
            <>
              <h2 className="text-lg mb-4 text-white">What kind of care do you need?</h2>
              <select
                name="careType"
                value={formData.careType}
                onChange={handleChange}
                className="w-full p-4 rounded-lg border border-gray-300 text-gray-900 focus:outline-none focus:ring-2 focus:ring-teal-400 mb-4"
              >
                <option value="" disabled>Select care type</option>
                <option value="Child care">Child care</option>
                <option value="Senior care">Senior care</option>
                <option value="Housekeeping">Housekeeping</option>
                <option value="Pet care">Pet care</option>
                <option value="Tutoring">Tutoring</option>
              </select>
              {errors.careType && <p className="text-red-500 text-sm mb-2">{errors.careType}</p>}
            </>
          );
        case 4:
          return (
            <>
              <h2 className="text-lg mb-4 text-white">Who needs care?</h2>
              <select
                name="whoNeedsCare"
                value={formData.whoNeedsCare}
                onChange={handleChange}
                className="w-full p-4 rounded-lg border border-gray-300 text-gray-900 focus:outline-none focus:ring-2 focus:ring-teal-400 mb-4"
              >
                <option value="" disabled>Select person</option>
                <option value="Myself">Myself</option>
                <option value="Family Member">Family Member</option>
                <option value="Friend">Friend</option>
              </select>
              {errors.whoNeedsCare && <p className="text-red-500 text-sm mb-2">{errors.whoNeedsCare}</p>}
            </>
          );
        case 5:
          return (
            <>
              <h2 className="text-lg mb-4 text-white">Do you need any other type of care?</h2>
              <div className="mb-4 text-white">
                {['Child care', 'Senior care', 'Housekeeping', 'Pet care'].map((care) => (
                  <label key={care} className="flex items-center mb-2">
                    <input
                      type="checkbox"
                      name="additionalCare"
                      value={care}
                      checked={formData.additionalCare.includes(care)}
                      onChange={handleCheckboxChange}
                      className="mr-2"
                    />
                    {care}
                  </label>
                ))}
              </div>
              {errors.additionalCare && <p className="text-red-500 text-sm mb-2">{errors.additionalCare}</p>}
            </>
          );
        case 6:
          return (
            <>
              <h2 className="text-lg mb-4 text-white">Enter your email for verification</h2>
              <input
                type="email"
                name="email"
                placeholder="Email address"
                value={formData.email}
                onChange={handleChange}
                className="w-full p-4 rounded-lg border border-gray-300 text-gray-900 focus:outline-none focus:ring-2 focus:ring-teal-400 mb-4"
              />
              {errors.email && <p className="text-red-500 text-sm mb-2">{errors.email}</p>}
            </>
          );
        default:
          return null;
      }
    } else {
      switch (step) {
        case 1:
          return (
            <>
              <h2 className="text-lg mb-4 text-white">Enter Your ZIP Code</h2>
              <input
                type="text"
                name="zipCode"
                placeholder="ZIP code"
                value={formData.zipCode}
                onChange={handleChange}
                className="w-full p-4 rounded-lg border border-gray-300 text-gray-900 focus:outline-none focus:ring-2 focus:ring-teal-400 mb-4"
              />
              {errors.zipCode && <p className="text-red-500 text-sm mb-2">{errors.zipCode}</p>}
            </>
          );
        case 2:
          return (
            <>
              <h2 className="text-lg mb-4 text-white">Create Your Profile</h2>
              <input
                type="text"
                name="firstName"
                placeholder="First Name"
                value={formData.firstName}
                onChange={handleChange}
                className="w-full p-4 rounded-lg border border-gray-300 text-gray-900 focus:outline-none focus:ring-2 focus:ring-teal-400 mb-4"
              />
              {errors.firstName && <p className="text-red-500 text-sm mb-2">{errors.firstName}</p>}
              <input
                type="text"
                name="lastName"
                placeholder="Last Name"
                value={formData.lastName}
                onChange={handleChange}
                className="w-full p-4 rounded-lg border border-gray-300 text-gray-900 focus:outline-none focus:ring-2 focus:ring-teal-400 mb-4"
              />
              {errors.lastName && <p className="text-red-500 text-sm mb-2">{errors.lastName}</p>}
            </>
          );
        case 3:
          return (
            <>
              <h2 className="text-lg mb-4 text-white">Verify Your Email</h2>
              <input
                type="email"
                name="email"
                placeholder="Email address"
                value={formData.email}
                onChange={handleChange}
                className="w-full p-4 rounded-lg border border-gray-300 text-gray-900 focus:outline-none focus:ring-2 focus:ring-teal-400 mb-4"
              />
              {errors.email && <p className="text-red-500 text-sm mb-2">{errors.email}</p>}
            </>
          );
        default:
          return null;
      }
    }
  };

  return (
    <div className="relative h-screen bg-gray-100">
      {/* Background Video */}
      <video
        className="absolute top-0 left-0 w-full h-full object-cover"
        src="https://wedesignthemes.s3.amazonaws.com/thatha/Slider+VDO+02+HD.mp4"
        autoPlay
        loop
        muted
      />
      
      {/* Overlay */}
      <div className="absolute top-0 left-0 w-full h-full bg-black opacity-60"></div>
      
      {/* Content */}
      <div className="relative z-10 h-full w-full flex items-center justify-start">
        <div className="w-full max-w-5xl px-4 lg:px-8">
          <div className="text-white">
            <div className="bg-white bg-opacity-30 p-8 rounded-lg shadow-lg">
              <div className="flex gap-4 items-center mb-6">
                <button 
                  className={`font-semibold pb-2 border-b-2 ${activeForm === 'care' ? 'text-teal-400 border-teal-400' : 'text-white border-transparent hover:border-white'}`}
                  onClick={() => { setActiveForm('care'); resetForm(); }}
                >
                  Find care
                </button>
                <button 
                  className={`font-semibold pb-2 border-b-2 ${activeForm === 'job' ? 'text-teal-400 border-teal-400' : 'text-white border-transparent hover:border-white'}`}
                  onClick={() => { setActiveForm('job'); resetForm(); }}
                >
                  Find a job
                </button>
              </div>
              
              {/* Form */}
              <div className="mb-6">
                {renderForm()}
                <button 
                  onClick={handleNext} 
                  className="bg-teal-600 hover:bg-teal-700 text-white px-8 py-3 rounded-lg transition duration-300 w-full"
                >
                  {(activeForm === 'care' && step === 6) || (activeForm === 'job' && step === 3) ? 'Submit' : 'Next'}
                </button>
              </div>
              
              {/* Icon Section */}
              {step === 1 && (
                <div className="grid grid-cols-2 sm:grid-cols-3 lg:grid-cols-5 gap-6">
                  {[
                    { icon: FaChild, label: 'Child care' },
                    { icon: FaHeart, label: 'Senior care' },
                    { icon: FaHome, label: 'Housekeeping' },
                    { icon: FaPaw, label: 'Pet care' },
                    { icon: FaBook, label: 'Tutoring' },
                  ].map((item, index) => (
                    <div key={index} className="text-center">
                      <item.icon className="mx-auto mb-2 text-teal-400 text-4xl" />
                      <p className="text-sm text-white font-medium">{item.label}</p>
                    </div>
                  ))}
                </div>
              )}
            </div>
          </div>
        </div>
      </div>

      {/* Success Toast */}
      
    </div>
  );
};

export default HeroSection;